class SelectItem<T> extends ListTile {
  final T value;
  final String search;
  @override
  final Widget title;
  @override
  final void Function()? onTap;

  const SelectItem({
    Key? key,
    required this.value,
    required this.search,
    required this.title,
    this.onTap,
  }) : super(
          key: key,
          title: title,
          onTap: onTap,
        );

  SelectItem<T> copyWith({
    T? value,
    String? search,
    Widget? title,
    void Function()? onTap,
  }) {
    return SelectItem<T>(
      value: value ?? this.value,
      search: search ?? this.search,
      title: title ?? this.title,
      onTap: onTap ?? this.onTap,
    );
  }
}

